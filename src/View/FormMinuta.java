    /**
     * Método preenche a tabela do formConsultar (Minutas)
     * @param modelo 
     */
    private void preencherTabela(DefaultTableModel modelo) {
        String sql = "SELECT ID_MINUTA, R.NOME, D.NOME, D.CIDADE, DATA_EMISSAO FROM MINUTA M, "
                + "REMETENTE R, DESTINATARIO D WHERE M.ID_REMETENTE = R.ID_REMETENTE AND "
                + "M.ID_DESTINATARIO = D.ID_DESTINATARIO ORDER BY 1 DESC";
        this.conex.connect();
        
        try {
            PreparedStatement pst = this.conex.c.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            DefaultTableModel model = modelo;
            
            while (model.getRowCount() > 0)  model.removeRow(0);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)});
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível encontrar as minutas.", "Atenção", JOptionPane.WARNING_MESSAGE);
        } finally {
            this.conex.disconnect();
        }
    }
        

